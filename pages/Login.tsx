
import React, { useState, useEffect, useRef } from 'react';
import { useNavigate } from 'react-router-dom';
import { mockAuth } from '../services/mockFirebase';

type LoginStep = 'identity' | 'sending' | 'verifying' | 'success';

const Login: React.FC = () => {
  const [step, setStep] = useState<LoginStep>('identity');
  const [email, setEmail] = useState('');
  const [otp, setOtp] = useState(['', '', '', '', '', '']);
  const [loading, setLoading] = useState(false);
  const [timer, setTimer] = useState(60);
  const [error, setError] = useState('');
  const navigate = useNavigate();
  const otpRefs = useRef<(HTMLInputElement | null)[]>([]);

  // Validation: Must be a gmail address
  const isValidGmail = (e: string) => {
    return /^[a-zA-Z0-9._%+-]+@gmail\.com$/.test(e);
  };

  // Timer logic for resend
  useEffect(() => {
    let interval: any;
    if (step === 'verifying' && timer > 0) {
      interval = setInterval(() => setTimer((t) => t - 1), 1000);
    }
    return () => clearInterval(interval);
  }, [step, timer]);

  const handleIdentitySubmit = (e: React.FormEvent) => {
    e.preventDefault();
    if (!isValidGmail(email)) {
      setError('Please enter a valid @gmail.com address');
      return;
    }
    setError('');
    setLoading(true);
    setStep('sending');
    
    // Simulate SMTP delivery delay
    setTimeout(() => {
      setLoading(false);
      setStep('verifying');
    }, 1800);
  };

  const handleGoogleLogin = () => {
    setLoading(true);
    setStep('sending');
    // Simulate Google OAuth handshake
    setTimeout(() => {
      setEmail('student.community@gmail.com');
      setLoading(false);
      setStep('verifying');
    }, 1500);
  };

  const handleOtpChange = (index: number, value: string) => {
    if (isNaN(Number(value))) return;
    const newOtp = [...otp];
    newOtp[index] = value.substring(value.length - 1);
    setOtp(newOtp);

    // Move to next input
    if (value && index < 5) {
      otpRefs.current[index + 1]?.focus();
    }

    // Check if complete
    if (newOtp.every(digit => digit !== '')) {
      verifyCode(newOtp.join(''));
    }
  };

  const handleKeyDown = (index: number, e: React.KeyboardEvent<HTMLInputElement>) => {
    if (e.key === 'Backspace' && !otp[index] && index > 0) {
      otpRefs.current[index - 1]?.focus();
    }
  };

  const verifyCode = (code: string) => {
    setLoading(true);
    // Simulate server-side verification check
    setTimeout(() => {
      setLoading(false);
      // In this mock, we accept '123456' or any 6 digits for demonstration
      setStep('success');
      mockAuth.login(email || 'verified.user@gmail.com');
      setTimeout(() => navigate('/'), 2000);
    }, 1500);
  };

  return (
    <div className="min-h-[85vh] flex items-center justify-center px-4 py-16 bg-gradient-to-b from-accent/10 via-white to-white">
      <div className="w-full max-w-md bg-white rounded-[3.5rem] shadow-[0_32px_64px_-12px_rgba(20,83,45,0.15)] border border-accent/10 p-8 md:p-14 relative overflow-hidden">
        
        {/* Step 1: Identity Entry */}
        {step === 'identity' && (
          <div className="animate-in fade-in slide-in-from-bottom-6 duration-700">
            <div className="text-center mb-10">
              <div className="w-24 h-24 bg-accent rounded-[2rem] flex items-center justify-center mx-auto mb-8 shadow-2xl shadow-accent/40 rotate-6 hover:rotate-0 transition-transform duration-500">
                <svg className="w-14 h-14 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d="M12 6.253v13m0-13C10.832 5.477 9.246 5 7.5 5S4.168 5.477 3 6.253v13C4.168 18.477 5.754 18 7.5 18s3.332.477 4.5 1.253m0-13C13.168 5.477 14.754 5 16.5 5c1.747 0 3.332.477 4.5 1.253v13C19.832 18.477 18.247 18 16.5 18c-1.746 0-3.332.477-4.5 1.253" />
                </svg>
              </div>
              <h1 className="text-4xl font-black text-black mb-3 tracking-tight">BookSwap</h1>
              <p className="text-black/40 font-bold uppercase tracking-[0.15em] text-[10px]">Bangladesh's Student Marketplace</p>
            </div>

            <button 
              onClick={handleGoogleLogin}
              disabled={loading}
              className="w-full flex items-center justify-center gap-3 bg-white border-2 border-gray-100 py-4.5 rounded-2xl font-bold text-black hover:bg-gray-50 hover:border-accent/30 transition-all active:scale-[0.97] disabled:opacity-50 shadow-sm"
            >
              <svg className="w-6 h-6" viewBox="0 0 24 24">
                <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4"/>
                <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853"/>
                <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05"/>
                <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335"/>
              </svg>
              Sign in with Google
            </button>

            <div className="flex items-center my-10">
              <div className="flex-1 h-px bg-gray-100"></div>
              <span className="px-5 text-[10px] font-black text-black/20 uppercase tracking-[0.25em]">or enter gmail</span>
              <div className="flex-1 h-px bg-gray-100"></div>
            </div>

            <form onSubmit={handleIdentitySubmit} className="space-y-5">
              <div className="relative group">
                <input 
                  required
                  type="email"
                  className={`w-full bg-gray-50 border-2 ${error ? 'border-red-100' : 'border-transparent'} rounded-2xl py-5 px-6 focus:ring-4 focus:ring-accent/10 focus:border-accent focus:bg-white transition-all text-black font-bold placeholder:text-black/20 outline-none`}
                  placeholder="name@gmail.com"
                  value={email}
                  onChange={e => setEmail(e.target.value)}
                />
                {error && <p className="text-[10px] text-red-500 font-bold mt-2 ml-2 uppercase tracking-wider">{error}</p>}
              </div>
              <button 
                type="submit"
                disabled={loading || !email}
                className="w-full bg-black text-white py-5 rounded-2xl font-bold text-lg shadow-2xl hover:bg-neutral-800 active:scale-[0.97] transition-all disabled:opacity-50"
              >
                Send Verification Code
              </button>
            </form>
          </div>
        )}

        {/* Step: Sending Animation */}
        {step === 'sending' && (
          <div className="text-center py-20 animate-in fade-in duration-500">
            <div className="relative w-24 h-24 mx-auto mb-10">
              <div className="absolute inset-0 bg-accent/20 rounded-full animate-ping"></div>
              <div className="relative bg-white border-4 border-accent w-full h-full rounded-full flex items-center justify-center text-accent">
                <svg className="w-10 h-10 animate-bounce" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M3 8l7.89 5.26a2 2 0 002.22 0L21 8M5 19h14a2 2 0 002-2V7a2 2 0 00-2-2H5a2 2 0 00-2 2v10a2 2 0 002 2z" />
                </svg>
              </div>
            </div>
            <h2 className="text-2xl font-bold text-black">Connecting to Gmail...</h2>
            <p className="text-black/40 mt-2 font-medium">Securing your identity</p>
          </div>
        )}

        {/* Step 2: OTP Verification */}
        {step === 'verifying' && (
          <div className="animate-in fade-in zoom-in-95 duration-500 text-center">
            <div className="w-20 h-20 bg-accent/5 rounded-full flex items-center justify-center mx-auto mb-8 text-accent border border-accent/10">
              <svg className="w-10 h-10" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 15v2m-6 4h12a2 2 0 002-2v-6a2 2 0 00-2-2H6a2 2 0 00-2 2v6a2 2 0 002 2zm10-10V7a4 4 0 00-8 0v4h8z" />
              </svg>
            </div>
            <h2 className="text-3xl font-black text-black mb-2">Verify it's you</h2>
            <p className="text-black/50 text-sm mb-12">
              We sent a 6-digit verification code to<br/>
              <span className="font-bold text-black border-b-2 border-accent/20">{email}</span>
            </p>

            <div className="flex justify-between gap-3 mb-12">
              {otp.map((digit, index) => (
                <input
                  key={index}
                  ref={el => otpRefs.current[index] = el}
                  type="text"
                  maxLength={1}
                  className="w-12 h-16 text-center text-3xl font-black bg-gray-50 border-2 border-transparent focus:border-accent focus:bg-white rounded-2xl transition-all outline-none shadow-inner"
                  value={digit}
                  onChange={e => handleOtpChange(index, e.target.value)}
                  onKeyDown={e => handleKeyDown(index, e)}
                  autoFocus={index === 0}
                />
              ))}
            </div>

            <div className="space-y-6">
              {loading ? (
                <div className="flex flex-col items-center gap-4">
                  <div className="w-8 h-8 border-4 border-accent border-t-transparent rounded-full animate-spin"></div>
                  <span className="text-accent font-black uppercase tracking-widest text-[10px]">Verifying Code</span>
                </div>
              ) : (
                <>
                  <p className="text-[11px] text-black/30 font-bold uppercase tracking-widest">
                    Didn't get the email? Check your spam folder
                  </p>
                  <button 
                    disabled={timer > 0}
                    onClick={() => {setTimer(60); setOtp(['','','','','','']);}}
                    className={`text-sm font-bold flex items-center justify-center gap-2 mx-auto ${timer > 0 ? 'text-black/20' : 'text-accent hover:underline decoration-2 underline-offset-4'}`}
                  >
                    Resend Code {timer > 0 && <span className="bg-gray-100 px-2 py-0.5 rounded-full text-[10px]">{timer}s</span>}
                  </button>
                </>
              )}
            </div>

            <button 
              onClick={() => setStep('identity')}
              className="mt-16 text-[10px] font-black text-black/20 hover:text-black transition-colors uppercase tracking-[0.3em] block mx-auto"
            >
              ‚Üê Use different email
            </button>
          </div>
        )}

        {/* Step 3: Success Reward */}
        {step === 'success' && (
          <div className="animate-in fade-in zoom-in-95 duration-700 text-center py-10">
            <div className="relative w-32 h-32 mx-auto mb-10">
              <div className="absolute inset-0 bg-accent rounded-full animate-ping opacity-20"></div>
              <div className="relative w-full h-full bg-accent rounded-full flex items-center justify-center shadow-2xl shadow-accent/50">
                <svg className="w-16 h-16 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={3} d="M5 13l4 4L19 7" />
                </svg>
              </div>
            </div>
            <h2 className="text-4xl font-black text-black mb-4">Gmail Verified</h2>
            <p className="text-black/40 font-bold uppercase tracking-widest text-xs">Security Check Complete</p>
            <div className="mt-14 flex flex-col items-center gap-3">
              <span className="text-black/20 font-bold text-[10px] uppercase tracking-[0.3em]">Entering Marketplace</span>
              <div className="flex gap-1.5">
                <div className="w-2.5 h-2.5 bg-accent rounded-full animate-bounce [animation-delay:-0.3s]"></div>
                <div className="w-2.5 h-2.5 bg-accent rounded-full animate-bounce [animation-delay:-0.15s]"></div>
                <div className="w-2.5 h-2.5 bg-accent rounded-full animate-bounce"></div>
              </div>
            </div>
          </div>
        )}
      </div>
    </div>
  );
};

export default Login;
